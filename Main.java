public class Main {

	public static void main(String[] args) {
		/*
		 1. System.out.println("___") -> Display the Text on the Screen and move the cursor to the next line.
		 2. System.out.print("___") -> Display the Text only without Line Break.

		 Notes: 1. Text must be enclodsed into double quotes " " not single quotes ' '
		 		2. Character must be enclodsed into single quotes ' ' not double quotes " "
		        3. \n -> New Line
		*/

		System.out.println("Ahmed A Amer Start learning Java");

		System.out.print("Hello, World!\n"); // Equals System.out.println("Hello, World!");

		System.out.print("Learn Simply!");

	}

}